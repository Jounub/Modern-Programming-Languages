#ifdef TASK3
#include <iostream>
using namespace std;

void main(void) 
{
    int a = 2, b = 5;

    // Объявляем функции
    void obmen1(int, int);
    void obmen2(int*, int*);
    void obmen3(int&, int&);

    cout << "before exchange: a=" << a << ", b=" << b << endl;

    // Вызов obmen1 - передача по значению
    // Переменные a и b НЕ изменятся, так как функция работает с копиями
    obmen1(a, b);
    cout << "after exchange 1: a=" << a << ", b=" << b << endl;

    // Вызов obmen2 - передача по указателю
    // Передаём адреса переменных a и b
    obmen2(&a, &b);
    cout << "after exchange 2: a=" << a << ", b=" << b << endl;

    // Вызов obmen3 - передача по ссылке
    // Переменные a и b изменятся, так как передаются по ссылке
    obmen3(a, b);
    cout << "after exchange 3: a=" << a << ", b=" << b << endl;
}

// 1 вариант: передача по значению
// Этот вариант не сможет изменить оригинальные переменные
void obmen1(int x, int y) {
    int temp = x;
    x = y;
    y = temp;
    // Изменения происходят только внутри функции,
    // Здесь меняются только копии переменных, оригинальные переменные a и b не меняются
}

// 2 вариант: передача по указателю
// Функция получает адреса переменных и меняет значения по этим адресам
void obmen2(int* x, int* y) {
    int temp = *x;
    *x = *y;
    *y = temp;
    // Так как работаем напрямую с адресами, изменения будут видны в main()
}

// 3 вариант: передача по ссылке
// Функция получает ссылки на оригинальные переменные
void obmen3(int& x, int& y) {
    int temp = x;
    x = y;
    y = temp;
    // Ссылки работают как псевдонимы оригинальных переменных,
    // поэтому изменения будут видны в main()
}
#endif
